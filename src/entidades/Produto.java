package entidades;

/**
 *
 * @author AdrianoBenelliBastos
 */
public class Produto {
    
    private String produto;
    private String descricao;
    private String categoria;
    private String marca;
    private String modelo;
    private String origem;
    private double prcCompra;
    private double prcVenda;
    private int lmtestoque;
    private int estoque;

    
    
    
    
    
    
    /**
     * @return the produto
     */
    public String getProduto() {
        return produto;
    }

    /**
     * @param produto the produto to set
     */
    public void setProduto(String produto) {
        this.produto = produto;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the marca
     */
    public String getMarca() {
        return marca;
    }

    /**
     * @param marca the marca to set
     */
    public void setMarca(String marca) {
        this.marca = marca;
    }

    /**
     * @return the modelo
     */
    public String getModelo() {
        return modelo;
    }

    /**
     * @param modelo the modelo to set
     */
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    /**
     * @return the origem
     */
    public String getOrigem() {
        return origem;
    }

    /**
     * @param origem the origem to set
     */
    public void setOrigem(String origem) {
        this.origem = origem;
    }

    /**
     * @return the prcCompra
     */
    public double getPrcCompra() {
        return prcCompra;
    }

    /**
     * @param prcCompra the prcCompra to set
     */
    public void setPrcCompra(double prcCompra) {
        this.prcCompra = prcCompra;
    }

    /**
     * @return the prcVenda
     */
    public double getPrcVenda() {
        return prcVenda;
    }

    /**
     * @param prcVenda the prcVenda to set
     */
    public void setPrcVenda(double prcVenda) {
        this.prcVenda = prcVenda;
    }

    /**
     * @return the lmtestoque
     */
    public int getLmtestoque() {
        return lmtestoque;
    }

    /**
     * @param lmtestoque the lmtestoque to set
     */
    public void setLmtestoque(int lmtestoque) {
        this.lmtestoque = lmtestoque;
    }

    /**
     * @return the estoque
     */
    public int getEstoque() {
        return estoque;
    }

    /**
     * @param estoque the estoque to set
     */
    public void setEstoque(int estoque) {
        this.estoque = estoque;
    }
    
    
    
    
}
